package design.pattern.memorandum.example;

public class GameRole {
	/**
	 * 生命力
	 */
	private int vit;
	/**
	 * 攻击力
	 */
	private int attack;
	/**
	 * 防御力
	 */
	private int def;
	public int getVit() {
		return vit;
	}
	public void setVit(int vit) {
		this.vit = vit;
	}
	public int getAttack() {
		return attack;
	}
	public void setAttack(int attack) {
		this.attack = attack;
	}
	public int getDef() {
		return def;
	}
	public void setDef(int def) {
		this.def = def;
	}
	
}
